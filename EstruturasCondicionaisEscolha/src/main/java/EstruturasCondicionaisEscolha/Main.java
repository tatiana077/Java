package EstruturasCondicionaisEscolha;

public class Main {

    public static void main(String[] args) {
        int codigoPronto = 7;
        
        switch (codigoPronto) {
            case 10:
                System.out.println("numero 10");
                break;
            case 5:
                System.out.println("numero 5");
                break;
            default:
                System.out.println("erro, tente denovo.");
        }
     
 
    }
    
}
